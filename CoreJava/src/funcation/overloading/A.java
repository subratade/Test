package funcation.overloading;

public class A {
	
	public void m1(int a){
		System.out.println("Inside Integer of A class " + a);
	}
	public void m1(float a){
		System.out.println("Inside Float  of A class " + a);
	}
	public void m1(int a,int b){
		System.out.println("Inside Integer-Integer of A class " + a+b);
	}
	
}
